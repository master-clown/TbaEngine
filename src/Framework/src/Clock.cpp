#include <Framework/Clock.h>

//======================================================================================================================
using namespace framework;

//======================================================================================================================
Clock::Clock()
    : _startTime(ClockImpl::now())
{
}

//----------------------------------------------------------------------------------------------------------------------
Clock::~Clock() = default;

//======================================================================================================================
auto Clock::getCurrentTicksSinceStart() const -> Nanoseconds
{
    return std::chrono::duration_cast<Nanoseconds>(ClockImpl::now() - _startTime);
}
